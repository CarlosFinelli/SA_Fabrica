open class Calculadora {

    fun somar(x: Int, y: Int) : Int{
        return x + y
    }

    fun subtrair(x: Int, y: Int) : Int{
        return x - y
    }

    fun multiplicar(x: Int, y: Int) : Int{
        return x * y
    }

    fun dividir(x: Double, y: Double) : Double {
        return x / y
    }
}